import fs from 'node:fs';
import path from 'node:path';

const inputPath = process.argv[2];
const outputPath = path.resolve('services/modTalentData.ts');

if (!inputPath) {
  console.error('Usage: node scripts/import-mod-talent-csv.mjs <csv-path>');
  process.exit(1);
}

const normalizeKey = (value) =>
  String(value || '')
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .replace(/[^a-z0-9]/g, '');

const parseCSV = (text) => {
  const rows = [];
  let row = [];
  let cell = '';
  let quoted = false;

  for (let i = 0; i < text.length; i += 1) {
    const ch = text[i];
    const next = text[i + 1];

    if (ch === '"') {
      if (quoted && next === '"') {
        cell += '"';
        i += 1;
      } else {
        quoted = !quoted;
      }
    } else if (ch === ',' && !quoted) {
      row.push(cell);
      cell = '';
    } else if ((ch === '\n' || ch === '\r') && !quoted) {
      if (ch === '\r' && next === '\n') i += 1;
      row.push(cell);
      rows.push(row);
      row = [];
      cell = '';
    } else {
      cell += ch;
    }
  }

  row.push(cell);
  rows.push(row);
  return rows.filter((entry) => entry.some((item) => item.trim()));
};

const titleCaseCategory = (value) => {
  const normalized = String(value || '').trim();
  if (!normalized) return 'Creator';
  return normalized;
};

const mapTier = (value) => {
  const tier = String(value || '').trim().toUpperCase();
  if (tier === 'LEGEND') return 'ICON';
  if (['ICON', 'A_LIST', 'ESTABLISHED', 'RISING', 'INDIE', 'UNKNOWN'].includes(tier)) return tier;
  return 'ESTABLISHED';
};

const mapBias = (value) => {
  const bias = String(value || '').trim().toUpperCase();
  if (bias === 'PRESTIGE') return 'PRESTIGE';
  if (bias === 'COMMERCIAL') return 'COMMERCIAL';
  return 'MIXED';
};

const mapGender = (value) => {
  const gender = String(value || '').trim().toUpperCase();
  if (gender === 'MALE' || gender === 'FEMALE' || gender === 'NON_BINARY') return gender;
  return 'NON_BINARY';
};

const hashNumber = (value) => {
  let hash = 0;
  for (const ch of value) hash = ((hash << 5) - hash + ch.charCodeAt(0)) | 0;
  return Math.abs(hash);
};

const estimateAge = (row) => {
  const tier = mapTier(row.tier);
  const category = String(row.category || '').trim().toLowerCase();
  const hash = hashNumber(`${row.country}:${row.name}:${category}`);
  if (tier === 'ICON') return 48 + (hash % 34);
  if (tier === 'A_LIST') return 29 + (hash % 24);
  if (tier === 'RISING') return 19 + (hash % 13);
  if (category === 'director') return 35 + (hash % 31);
  return 24 + (hash % 29);
};

const toNumber = (value) => {
  const parsed = Number(String(value || '0').trim());
  return Number.isFinite(parsed) ? parsed : 0;
};

const source = fs.readFileSync(inputPath, 'utf8');
const rows = parseCSV(source);
const header = rows.shift()?.map((item) => item.trim()) || [];
const required = [
  'country',
  'category',
  'name',
  'gender',
  'known_for',
  'estimated_followers_m',
  'estimated_net_worth_m',
  'prestige_bias',
  'tier',
  'forbes_category',
  'notes'
];

for (const key of required) {
  if (!header.includes(key)) {
    console.error(`Missing required CSV column: ${key}`);
    process.exit(1);
  }
}

const seen = new Set();
const cleaned = [];

for (const rawRow of rows) {
  const row = Object.fromEntries(header.map((key, idx) => [key, rawRow[idx] ?? '']));
  const name = String(row.name || '').trim();
  const country = String(row.country || '').trim();
  const category = String(row.category || '').trim().toLowerCase();
  const key = normalizeKey(name);

  if (!name || !country || !['actor', 'director', 'creator'].includes(category) || seen.has(key)) continue;
  seen.add(key);

  cleaned.push({
    country,
    category,
    name,
    gender: mapGender(row.gender),
    knownFor: String(row.known_for || '').trim(),
    followersM: toNumber(row.estimated_followers_m),
    netWorthM: toNumber(row.estimated_net_worth_m),
    prestigeBias: mapBias(row.prestige_bias),
    tier: mapTier(row.tier),
    forbesCategory: titleCaseCategory(row.forbes_category),
    notes: String(row.notes || '').trim(),
    age: estimateAge(row)
  });
}

const output = `// Generated by scripts/import-mod-talent-csv.mjs. Do not edit by hand.
import { Gender, NPCPrestige, NPCTier } from '../types';

export type ModTalentCategory = 'actor' | 'director' | 'creator';

export interface ModTalentRow {
  country: string;
  category: ModTalentCategory;
  name: string;
  gender: Exclude<Gender, 'ALL'>;
  knownFor: string;
  followersM: number;
  netWorthM: number;
  prestigeBias: NPCPrestige;
  tier: NPCTier;
  forbesCategory: string;
  notes: string;
  age: number;
}

export const MOD_TALENT_ROWS: ModTalentRow[] = ${JSON.stringify(cleaned, null, 2)};
`;

fs.writeFileSync(outputPath, output);

const counts = cleaned.reduce((acc, row) => {
  const key = `${row.country} ${row.category}`;
  acc[key] = (acc[key] || 0) + 1;
  return acc;
}, {});

console.log(`Wrote ${cleaned.length} rows to ${outputPath}`);
console.log(JSON.stringify(counts, null, 2));
